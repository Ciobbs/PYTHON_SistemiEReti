def main():
    int = 42
    stringa = "Ciao, mondo!"
    float = 3.14
    bool = True

    print("Valore della variabile int:", int)
    print("Tipo della variabile int:", type(int))

    print("Valore della variabile stringa:", stringa)
    print("Tipo della variabile stringa:", type(stringa))

    print("Valore della variabile float:", float)
    print("Tipo della variabile float: ", type(float)) 

    print("Valore della variabile bool:", bool)
    print("Tipo della variabile bool: ", type(bool)) 
if __name__ == "__main__":
    main()