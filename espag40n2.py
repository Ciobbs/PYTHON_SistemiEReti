num = int(input("Inserisci un numero: "))
if num % 2 == 0:
    print("Il numero {num} è divisibile per 2")
else:
    print("Il numero {num} non è divisibile per 2")
if num % 3 == 0:
    print("Il numero {num} è divisibile per 3")
else:
    print("Il numero {num} non è divisibile per 3")
if num % 5 == 0:
    print("Il numero {num} è divisibile per 5")
else:
    print("Il numero {num} non è divisibile per 5")

 
